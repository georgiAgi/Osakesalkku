    /**
     * Metodi asettaa osakkeen tiedot käyttäjän nähtäville.
     */
    public void asetaTiedot() {
        nimiLabel.setText("Muuta osakkeen " + osake.getNimi() + " tietoja");
        nykNimiLabel.setText(osake.getNimi());
        nykArvoLabel.setText(df2.format(osake.getHinta()) + " €");
        nykAlaLabel.setText(osake.getToimiala().toString());

        nykBetaLabel.setText(df2.format(osake.getRiski().getBeta()));
        nykVolaLabel.setText(df2.format(osake.getRiski().getVolatiliteetti()));
    }

    private boolean onNumeerinenJaPositiivinen(String str) {
        try {
            double d = Double.parseDouble(str);
            if (d < 0) {
                return false;
            }
        } catch (NumberFormatException nfe) {
            return false;
        }
        return true;
    }
    
