    private void muutaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_muutaButtonActionPerformed
        // TODO add your handling code here:
        String nimi = nimiTextField.getText();
        String arvoTxt = hintaTextField.getText();
        String betaTxt = betaTextField.getText();
        String volaTxt = volaTextField.getText();

        if (!onNumeerinenJaPositiivinen(arvoTxt) || arvoTxt.isEmpty()) {
            if (!arvoTxt.isEmpty()) {
                tietoVaarassaMuodossa();
                return;
            }
        } else {
            osake.setHinta(Double.parseDouble(arvoTxt));
        }

        if (!onNumeerinenJaPositiivinen(betaTxt) || betaTxt.isEmpty()) {
            if (!betaTxt.isEmpty()) {
                tietoVaarassaMuodossa();
                return;
            }
        } else {
            osake.getRiski().setBeta(Double.parseDouble(betaTxt));
        }

        if (!onNumeerinenJaPositiivinen(volaTxt) || volaTxt.isEmpty()) {
            if (!volaTxt.isEmpty()) {
                tietoVaarassaMuodossa();
                return;
            }
        } else {
            osake.getRiski().setVolatiliteetti(Double.parseDouble(volaTxt));
        }

        if (!nimi.isEmpty()) {
            osake.setNimi(nimi);
        }

        osake.setToimiala((Toimiala) toimialaComboBox.getSelectedItem());

        vanhempi.paivitaLista();

        super.dispose();
    }//GEN-LAST:event_muutaButtonActionPerformed

